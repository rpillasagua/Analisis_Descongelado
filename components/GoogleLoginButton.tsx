'use client';

import { useState, useEffect } from 'react';
import { googleAuthService } from '@/lib/googleAuthService';
import { LogIn } from 'lucide-react';

interface GoogleLoginButtonProps {
  onLoginSuccess?: () => void;
}

export default function GoogleLoginButton({ onLoginSuccess }: GoogleLoginButtonProps) {
  const [isLoading, setIsLoading] = useState(false);
  const [isInitializing, setIsInitializing] = useState(true);

  useEffect(() => {
    // Inicializar Google Auth
    const init = async () => {
      try {
        await googleAuthService.initialize();
        
        // Verificar si ya hay sesión
        if (googleAuthService.isAuthenticated()) {
          onLoginSuccess?.();
        }
      } catch (error) {
        console.error('Error inicializando Google Auth:', error);
      } finally {
        setIsInitializing(false);
      }
    };

    init();
  }, [onLoginSuccess]);

  const handleLogin = async () => {
    setIsLoading(true);
    try {
      await googleAuthService.login();
      // El callback de Google Auth llamará a onLoginSuccess
      setTimeout(() => {
        if (googleAuthService.isAuthenticated()) {
          onLoginSuccess?.();
        } else {
          console.error('Autenticación no completada después de 1 segundo');
        }
        setIsLoading(false);
      }, 1000);
    } catch (error: any) {
      console.error('Error al iniciar sesión:', error);
      const errorMessage = error?.message || 'Error desconocido';
      alert(`Error al iniciar sesión con Google:\n\n${errorMessage}\n\nVerifica que la URL de Vercel esté autorizada en Google Cloud Console.`);
      setIsLoading(false);
    }
  };

  if (isInitializing) {
    return (
      <div className="flex items-center justify-center gap-2 px-6 py-3 bg-gray-200 dark:bg-gray-700 rounded-lg">
        <div className="animate-spin rounded-full h-5 w-5 border-b-2 border-gray-600"></div>
        <span className="text-gray-600 dark:text-gray-300">Cargando...</span>
      </div>
    );
  }

  return (
    <button
      onClick={handleLogin}
      disabled={isLoading}
      className="flex items-center justify-center gap-3 px-6 py-3 bg-white dark:bg-gray-800 border-2 border-gray-300 dark:border-gray-600 rounded-lg hover:bg-gray-50 dark:hover:bg-gray-700 transition-all shadow-md hover:shadow-lg disabled:opacity-50 disabled:cursor-not-allowed"
    >
      {isLoading ? (
        <>
          <div className="animate-spin rounded-full h-5 w-5 border-b-2 border-blue-600"></div>
          <span className="text-gray-700 dark:text-gray-300 font-medium">Iniciando sesión...</span>
        </>
      ) : (
        <>
          <svg className="w-5 h-5" viewBox="0 0 24 24">
            <path
              fill="#4285F4"
              d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
            />
            <path
              fill="#34A853"
              d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
            />
            <path
              fill="#FBBC05"
              d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
            />
            <path
              fill="#EA4335"
              d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
            />
          </svg>
          <span className="text-gray-700 dark:text-gray-300 font-medium">Iniciar sesión con Google</span>
        </>
      )}
    </button>
  );
}
